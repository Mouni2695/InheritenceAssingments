package inheritence;

public class Switchboard {

		private String color;
		private String brand;
		private String type;   //circle,rectangular,square
		private int swithes;
		private int regulators;
		private int sackets;
		public String getColor() {
			return color;
		}
		public void setColor(String color) {
			this.color = color;
		}
		public String getBrand() {
			return brand;
		}
		public void setBrand(String brand) {
			this.brand = brand;
		}
		public String getType() {
			return type;
		}
		public void setType(String type) {
			this.type = type;
		}
		public int getSwithes() {
			return swithes;
		}
		public void setSwithes(int swithes) {
			this.swithes = swithes;
		}
		public int getRegulators() {
			return regulators;
		}
		public void setRegulators(int regulators) {
			this.regulators = regulators;
		}
		public int getSackets() {
			return sackets;
		}
		public void setSackets(int sackets) {
			this.sackets = sackets;
		}
}
